import java.util.*;
public class Datatype{
    public static int add(int a,int b){
        return a+b;
    }
    public static float add(float a,float b){
        return a+b;
    }
    
    public static void main(String args[]){
        System.out.println(add(5,8));
        System.out.println(add(5.2f,8.5f));
    }
}